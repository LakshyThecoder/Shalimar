export function formatPrice(price) {
  return `€${Number(price).toFixed(2).replace(".", ",")}`;
}

export function getItemDesc(item, lang) {
  if (!item?.desc) return "";
  if (item.desc[lang]) return item.desc[lang];
  if (lang === "it") return item.desc.it || item.desc.en || "";
  return item.desc.en || item.desc.it || "";
}
